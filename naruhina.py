async def is_admin(chat_id: int, user_id: int, bot: TelegramBot) -> bool:
    """Check if user is admin"""
    member = await bot.get_chat_member(chat_id, user_id)
    if member:
        return member.get("status") in ["administrator", "creator"]
    return False

